    "\n            + '</tr>';"
    "\n        }).join('');"
    "\n      } catch(e) {"
    "\n        document.getElementById('ledger-body').innerHTML ="
    "\n          '<tr><td colspan=\"6\" class=\"empty\">Error: ' + e.message + '</td></tr>';"
    "\n      }"
    "\n    }"
    "\n"
    "\n    // ----------------------------------------------------------------"
    "\n    // Fetch and render Telemetry Spans"
