    "\n      try { return new Date(ts * 1000).toISOString().replace('T',' ').slice(0,23); }"
    "\n      catch(e) { return String(ts); }"
    "\n    }"
    "\n    function fmtJson(obj) {"
    "\n      if (obj === null || obj === undefined) return '';"
    "\n      if (typeof obj === 'string') return obj;"
    "\n      try { return JSON.stringify(obj); } catch(e) { return String(obj); }"
    "\n    }"
